// Exemplo 02 Verificando o tipo de variável

const valor = 50.5; // Constante do tipo double
const num = 100; // constante
const a = true ; // constante do tipo booleana
console.log(valor, `Tipo da constante ${typeof valor}`); // typeof exibe o tipo da constante ou variável
console.log(num, `${typeof num}`);
console.log(a, `${typeof a}`);
