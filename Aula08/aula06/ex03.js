/**
 * ex03_array_push_tres.js
 * Objetivo: criar um array vazio, adicionar três inteiros com push(),
 * e exibir o array resultante.
 * Como executar: node ex03_array_push_tres.js
 */

// 1) Cria um array vazio
const numeros = [];

// 2) Adiciona três inteiros
numeros.push(1);
numeros.push(2);
numeros.push(3);

// 3) Exibe o array resultante
console.log("Array final:", numeros);
